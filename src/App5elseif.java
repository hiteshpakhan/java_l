// logical operator 
// else if


public class App5elseif {
    public static void main(String[] args){
        // &&   -and
        // ||   -or
        
        int a = 5;
        int b = 4;
        
        
        
        if(a == 5 && b == 3){
            System.out.println("hi there");
        }else{
            System.out.println("hi there 2");
        }


        
        if(a == 5 || b == 3){
            System.out.println("hi there");
        }else{
            System.out.println("hi there 2");
        }


        if(a == 2){
            System.out.println("hi there");
        }else if(b == 4){
            System.out.println("hi there 2");
        }else{
            System.out.println("hi there 3");
        }



    }
}
