// Maths class
// maths class is inbuild in the java you can use it by following 

public class App2math {
    public static void main(String[] args){
        // to find the maximum
        // Math.max()
        System.out.println(Math.max(4, 13));

        // Math.min()
        System.out.println(Math.min(7,2));

        // Math.random()
        System.out.println(Math.random());  //it will give you a random value

        // random print the value in the long form but you can also convert it into int
        System.out.println((int)Math.random());

        // but if you want to print a random number 
        System.out.println((int)(Math.random()*100));

        
    }
}